my_string = input("Введите ваш текст: ")
print(my_string) #изначальный текст
print(len(my_string)) # кол-во символов
print(my_string .upper()) # текст в верхнем регистре
print(my_string .lower()) # текст в нижнем регистре
print(my_string .replace(" ","")) # текст без пробелов
print(my_string[0]) # первый символ текста
print(my_string[-1]) # последний символ текста
